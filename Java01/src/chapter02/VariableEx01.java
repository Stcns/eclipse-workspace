package chapter02;

public class VariableEx01 {

	public static void main(String[] args) {
		
		int level;			// 변수를 선언 후
		level = 10;			// 값을 할당
		level = 50;			// 값이 할당된 level 변수에 재 할당
		level = 1000;
		
		int level2 = 20;	// 변수 선언과 동시에 값을 할당
		level2 = 100;
		level2 = 2000;
		
		
		System.out.println(level);
		System.out.println(level2);
		
	}

}
